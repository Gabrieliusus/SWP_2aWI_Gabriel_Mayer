
let randomNumber = Math.random() * 6 + 5;
let random = Math.floor(randomNumber);

if (random == 10) {
    console.log("Ten")
}
else if (random == 9) {
    console.log("Nine")
}
else if (random == 8) {
    console.log("Eight")
}
else if (random == 7) {
    console.log("Seven")
}
else if (random == 6) {
    console.log("Six")
}
else if (random == 5) {
    console.log("Five")
}